<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletes;

/**
 * Class Module
 * @package App\Models
 *
 * @property int id
 * @property int course_id
 * @property int position
 * @property string name
 */
class Module extends Model
{
    use SoftDeletes;

    public const TABLE = "modules";

    protected $table = self::TABLE;

    protected $fillable = [
        'name'
    ];

    protected static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub
        self::creating(function (Module $module) {
            $module->position = self::query()->where('course_id', $module->course_id)->max('position') + 1;
        });
    }

    public function course()
    {
        return $this->belongsTo('App\Models\Course', 'course_id', 'id')->get();
    }

    public function lessons()
    {
        return $this->hasMany("App\Models\Lesson", "module_id", "id")->get();
    }
}
